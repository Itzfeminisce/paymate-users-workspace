// Delay children animations in a controlled way
export const createDelayedChildAnimations = (baseDelay: number = 0.1) => {
  return Array.from({ length: 10 }).reduce((acc, _, i) => {
    acc[`child${i + 1}`] = {
      hidden: { opacity: 0, y: 20 },
      visible: {
        opacity: 1,
        y: 0,
        transition: {
          duration: 0.5,
          ease: [0.22, 1, 0.36, 1],
          delay: baseDelay * (i + 1),
        },
      },
    };
    return acc;
  }, {} as Record<string, Variants>);
};

